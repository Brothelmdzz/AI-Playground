def get_role_prompt(role_name: str) -> str:
    """
    获取角色专属提示词

    Args:
        role_name: 角色名称

    Returns:
        角色策略提示词，如果角色不存在则返回通用提示
    """
    return ROLE_PROMPTS.get(role_name, ROLE_PROMPTS.get("平民", ""))
